    private boolean validaLinha(int linha) {
        return (linha >= 0) && (linha <= jTable.getRowCount()); //Para descobrir o número de linhas de um JTable durante a execução usamos o método getRowCount()

    }

    private boolean validaColuna(int coluna) {
        return (coluna >= 0) && (coluna <= jTable.getColumnCount()); //Para descobrir o número de colunas de um JTable durante a execução usamos o método getColumnCount()

    }

    private boolean validaLinhaColuna(int linha, int coluna) {
        return validaLinha(linha) && validaColuna(coluna);
    }

    
