    private void botaoInserirLinhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoInserirLinhaActionPerformed
        String nome = fieldNome.getText();
        String endereco = fieldEndereco.getText();
        String telefone = fieldTelefone.getText();
        
        modeloTabela.addRow(new Object[]{contador++, nome, endereco, telefone});
    }//GEN-LAST:event_botaoInserirLinhaActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
       int linha = Integer.parseInt(fieldLinha.getText());
       int coluna = Integer.parseInt(fieldColuna.getText());
       String valor = fieldValor.getText();
       if (validaLinhaColuna(linha, coluna)) {
           jTable.setValueAt(valor, linha, coluna);
       }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void botaoLerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoLerActionPerformed
       int linha = Integer.parseInt(fieldLinha2.getText());
       int coluna = Integer.parseInt(fieldColuna2.getText());
       if (validaLinhaColuna(linha, coluna)) {
            fieldValor2.setText("" + jTable.getValueAt(linha, coluna));
        }
    }//GEN-LAST:event_botaoLerActionPerformed

    private void botaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverActionPerformed
        int linhaSelecioanda = jTable.getSelectedRow();
        if (validaLinha(linhaSelecioanda)) {
            modeloTabela.removeRow(linhaSelecioanda);
        }
    }//GEN-LAST:event_botaoRemoverActionPerformed

    private void botaoApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoApagarActionPerformed
       while (jTable.getRowCount() > 0) {
            modeloTabela.removeRow(0);
        }
    }//GEN-LAST:event_botaoApagarActionPerformed

