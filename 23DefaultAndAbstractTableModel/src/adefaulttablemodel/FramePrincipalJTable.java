    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        int linha = jTable.getSelectedRow();
        int coluna = jTable.getSelectedColumn();
        if (validaLinhaColuna(linha, coluna)) {
            fieldLinha2.setText("" + linha);
            fieldLinha.setText("" + linha);
            
            fieldColuna.setText("" + coluna);
            fieldColuna2.setText("" + coluna);
            
            fieldValor2.setText("" + jTable.getValueAt(linha, coluna));
        }
    }//GEN-LAST:event_jTableMouseClicked

